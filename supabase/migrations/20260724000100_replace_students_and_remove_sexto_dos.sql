    select 1 from public.grades
    where id = '_0031' and sede_id = '_0002'
  ) then
    raise exception 'Importación cancelada: no fue posible crear o identificar QUINTO-UNO de SAN RAFAEL';
  end if;

  select count(*)
  into missing_grade_count
  from imported_students imported
  left join public.grades grade_record on grade_record.id = imported.grade_id
  where grade_record.id is null;

  if missing_grade_count > 0 then
    raise exception 'Importación cancelada: % estudiantes no tienen un grado válido', missing_grade_count;
  end if;
end
$$;

